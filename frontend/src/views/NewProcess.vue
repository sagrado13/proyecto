<template>
  <div>
    <h1>Nuevo proceso</h1>
    <textarea v-model="message" name="message" placeholder="DescripciÃ³n detallada del caso"></textarea>
    <br />
    <button id="send" @click="addNewProcess()">Enviar</button>
  </div>
</template>

<script>
// Importamos axios
import axios from "axios";
// Importamos sweetalert2
import Swal from "sweetalert2";
// IMPORTAMOS FUNCIONES
import { isLoggedInUser } from "../api/utils";
import { getIdToken } from "../api/utils";
export default {
  name: "NewProcess",
  data() {
    return {
      message: "",
    };
  },
  methods: {
    async addNewProcess() {
      try {
        let token = localStorage.getItem("AUTH_TOKEN_KEY");
        axios.defaults.headers.common["Authorization"] = `${token}`;
        const response = await axios.post(
          "http://localhost:3000/users/" +
            getIdToken(token) +
            "/processes/lawyers/" +
            this.$route.params.id,
          {
            message: this.message,
          }
        );
        Swal.fire({
          icon: "success",
          title: `${response.data.message}`,
        });
        this.$route.push(window.history.back());
      } catch (error) {
        console.log(error);
        Swal.fire({
          icon: "error",
          title: `${error.response.data.message}`,
        });
      }
    },
  },
};
</script>

